/**
 * _memset - fill memory with constant byte
 * @s: memory area pointer
 * @b: byte to fill *s
 * @n: byte amount to be filled
 * Return: (s) memory area s pointer
 */
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < n; i++)
		s[i] = b;
	return (s);
}

/**
 * ffree - it frees string of strings
 * @pp: the string of strings
 */
void ffree(char **pp)
{
	char **a = pp;

	if (!pp)
		return;
	while (*pp)
		free(*pp++);
	free(a);
}

